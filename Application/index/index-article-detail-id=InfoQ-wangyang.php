        <h3><b>技术之道</b></h3>
        <p>一直以来，自己一直在探索“如何才能把技术做的更好”。因为很多时候发现，不是每个公司的项目都会很有技术含量，也不是每个人都有机会去接触到百万、千万，甚至上亿用户量的产品。

很多的技术朋友在做了几年的“增删查改”之后，发现： 除了把常用的技术点用的很熟悉以为，很快就开始迷茫了。当然，包括我自己。自己也不断的捣鼓很多的技术，包括最新的技术点，但是依然发现：学习到的知识 ≠ 能力，毕竟没有经过实战和检验。</p>
<p>也曾经想去拜大牛为师，后来发现：大牛都很忙，没空搭理自己。其实后来也慢慢发现：<b> 技术还得靠自己去悟，没有人可以帮你思考，只有自己思考，总结出来的东西，才是深刻的，合适自己的。</b></p>
<img src="../../assets/images/infoQ-wangyang-2.jpg" style="float:left; width:40% ; height:40%;">

<p>这里引用一段我很喜欢的经典台词：金轮法王对杨过说：“杨兄弟，你年纪轻轻，就懂很多武学绝学，实属不易；但是，你花样很多， 没有形成自己的武学， 最后武学很难上一个层次， 很难成为真正的高手。”</p>
<div class="clear"></div>
<p>这一点，自己也很有感触，自从踏入技术圈子以来，每天都能看到不同的新技术的产生，用日新月异来形容，毫不为过。很多的技术出来，自己特别的感兴趣，也非常的喜欢去把玩一下，也乐此不疲。后来初步的发现：自己很累，也越发感觉到自己的能力太“泛”，不深入。</p>
<p>自己确实懂得了很多的工具，但是，都很浅，真正最后使用的不多，而且即便使用了，出现问题的时候，自己依然是“一头雾水”，甚至有时候发现A工具不行，遇到问题了，自己没有正面的解决，而是马上转向一个B工具（与A类似），最后发现B也有坑，于是不断的换…..</p>
<p>换句话说，自己学的技能，没有任何的“核心竞争力”，到头来，只是“了解，熟悉”，谁都可以替代自己。而且，自己也不可能永远那么精力旺盛，不断的去追新技术。</p>
<p>怎么破？？？</p>

          
          <h3> <b>守住寂寞</b></h3>
          <p>听过很多人都说“把一门技术学好，学精，其他的技术都是大同小异的”。这句话，听了无数遍，自己也不停的怎么说，但是，真的没有“真真正正”去坚守。</p>
          <p>很多时候就是这样的，“明明知道一个事情有正确的方法，但是自己还是选择比较作死的方式”。于是就开始死磕一门语言，几种最相关的技术，从语法层面，到操作系统层面，到编译原因层面，到内核…..</p>
          <p>说实话，有时候也发现很枯燥，也无趣，也不知道学完之后，是不是真的可以“内功长进”，但是一想到那种“泛泛的工具”，想到自己遇到很多技术难题时候那种窘况，依然咬着牙，死磕到底再说吧，姑且相信。</p>
          <h3><b>顿悟</b></h3>
          	<p>能力的提升，一定是经过不断的实践和思考的，一定是在量变引起质变的。</p>
          	<p>以前在很多时候，分析一个技术问题的时候，总是“感觉”，“我以为”，其实就是“猜”了。有时候相信，作为技术人员，一直被外人看来是“严谨，科学”的代名词，如果靠“蒙，猜”，真的不知道自己作为技术人员的那种“骨气和自豪感”在哪里。</p
          	>
          	<p>有时候，对于一个技术问题，虽然从网络上，同行那里得到了解决办法，但是自己依然会尝试去解释到底是什么原因，也会尝试理解这个解决办法是从那几个点发现问题，解决问题的。</p>
          	
